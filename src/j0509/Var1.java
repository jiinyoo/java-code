package j0509;

public class Var1 {
	public static void main(String[] args) {
		
		
		//세미콜론; => 하나의 명령문의 끝을 알린다.
		//변수 : 지금 당장 사용하지 않고 값을 두어야 할 때, 값을 담는 그릇
		       //:값이 변할 수 있다.
		//값이란 : 명령을 내릴 때 필요한 정보
		//변수는 메모리에 저장됨.
		//값의 형태가 여러가지가 있다.
		//문자열, 문자, 정수, 실수, 논리값
		//자료형에 따라서 값을 저장할 수 있다.
		//변수 만들기 => 변수 선언=>자료형 변수명;
		//정수형 변수만들기
		int num; //정수를 저장하는 num이라는 변수 선언
		//변수에 값을 넣기 => 변수에 값을 할당 => 변수명 =값;
		num=88;
		//변수의 내용을 출력하기
		System.out.println(num);
		
	}
}
